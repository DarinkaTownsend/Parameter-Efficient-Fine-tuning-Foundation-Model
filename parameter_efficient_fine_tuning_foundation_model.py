import numpy as np
import torch

def get_bounding_boxes_from_instance_mask(mask, min_area=10):
    """
    mask: numpy array H x W
          0 = background
          1,2,3,... = individual nuclei
    returns:
          boxes: Tensor [N, 4] in format x1, y1, x2, y2
          instance_ids: list of nucleus ids
    """
    boxes = []
    instance_ids = []

    ids = np.unique(mask)
    ids = ids[ids != 0]

    for instance_id in ids:
        binary = mask == instance_id
        area = binary.sum()

        if area < min_area:
            continue

        ys, xs = np.where(binary)

        x1, x2 = xs.min(), xs.max()
        y1, y2 = ys.min(), ys.max()

        boxes.append([x1, y1, x2, y2])
        instance_ids.append(instance_id)

    if len(boxes) == 0:
        return torch.zeros((0, 4), dtype=torch.float32), []

    boxes = torch.tensor(boxes, dtype=torch.float32)

    return boxes, instance_ids

