def binary_stack_to_instance_mask(binary_masks, threshold=0.5):
    """
    binary_masks: Tensor [N, H, W], logits or probabilities
    returns instance mask H x W
    """
    if isinstance(binary_masks, torch.Tensor):
        masks = torch.sigmoid(binary_masks).detach().cpu().numpy()
    else:
        masks = binary_masks

    instance_mask = np.zeros(masks.shape[1:], dtype=np.int32)

    for i, m in enumerate(masks):
        binary = m > threshold
        instance_mask[binary] = i + 1

    return instance_mask

def compute_iou_matrix(gt, pred):
    gt_ids = np.unique(gt)
    pred_ids = np.unique(pred)

    gt_ids = gt_ids[gt_ids != 0]
    pred_ids = pred_ids[pred_ids != 0]

    iou_matrix = np.zeros((len(gt_ids), len(pred_ids)), dtype=np.float32)

    for i, gid in enumerate(gt_ids):
        gt_mask = gt == gid

        for j, pid in enumerate(pred_ids):
            pred_mask = pred == pid

            intersection = np.logical_and(gt_mask, pred_mask).sum()
            union = np.logical_or(gt_mask, pred_mask).sum()

            if union > 0:
                iou_matrix[i, j] = intersection / union

    return iou_matrix, gt_ids, pred_ids

def compute_aji(gt, pred):
    iou_matrix, gt_ids, pred_ids = compute_iou_matrix(gt, pred)

    if len(gt_ids) == 0 and len(pred_ids) == 0:
        return 1.0

    if len(gt_ids) == 0 or len(pred_ids) == 0:
        return 0.0

    row_ind, col_ind = linear_sum_assignment(-iou_matrix)

    intersection_sum = 0
    union_sum = 0

    matched_gt = set()
    matched_pred = set()

    for r, c in zip(row_ind, col_ind):
        if iou_matrix[r, c] > 0:
            gid = gt_ids[r]
            pid = pred_ids[c]

            gt_mask = gt == gid
            pred_mask = pred == pid

            intersection_sum += np.logical_and(gt_mask, pred_mask).sum()
            union_sum += np.logical_or(gt_mask, pred_mask).sum()

            matched_gt.add(gid)
            matched_pred.add(pid)

    for gid in gt_ids:
        if gid not in matched_gt:
            union_sum += (gt == gid).sum()

    for pid in pred_ids:
        if pid not in matched_pred:
            union_sum += (pred == pid).sum()

    if union_sum == 0:
        return 0.0

    return intersection_sum / union_sum

def compute_pq(gt, pred, iou_threshold=0.5):
    iou_matrix, gt_ids, pred_ids = compute_iou_matrix(gt, pred)

    if len(gt_ids) == 0 and len(pred_ids) == 0:
        return 1.0

    if len(gt_ids) == 0 or len(pred_ids) == 0:
        return 0.0

    row_ind, col_ind = linear_sum_assignment(-iou_matrix)

    tp = 0
    sum_iou = 0
    matched_gt = set()
    matched_pred = set()

    for r, c in zip(row_ind, col_ind):
        if iou_matrix[r, c] >= iou_threshold:
            tp += 1
            sum_iou += iou_matrix[r, c]
            matched_gt.add(gt_ids[r])
            matched_pred.add(pred_ids[c])

    fp = len(pred_ids) - len(matched_pred)
    fn = len(gt_ids) - len(matched_gt)

    denominator = tp + 0.5 * fp + 0.5 * fn

    if denominator == 0:
        return 0.0

    return sum_iou / denominator

