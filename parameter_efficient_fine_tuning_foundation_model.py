from tqdm import tqdm

def train_one_epoch(model, dataloader, optimizer, device, max_boxes_per_image=20):
    model.train()
    total_loss = 0

    for batch in tqdm(dataloader):
        sample = batch[0]

        image = sample["image"].unsqueeze(0).to(device)
        mask = sample["mask"].to(device)
        boxes = sample["boxes"].to(device)
        instance_ids = sample["instance_ids"]

        if boxes.shape[0] == 0:
            continue

        # limitar cajas para ahorrar memoria
        if boxes.shape[0] > max_boxes_per_image:
            selected = torch.randperm(boxes.shape[0])[:max_boxes_per_image]
            boxes = boxes[selected]
            instance_ids = [instance_ids[i] for i in selected.cpu().numpy()]

        gt_instance_masks = get_instance_binary_masks(mask, instance_ids).to(device)

        optimizer.zero_grad()

        # image encoder
        image_embedding = model.image_encoder(image)

        # prompt encoder
        sparse_embeddings, dense_embeddings = model.prompt_encoder(
            points=None,
            boxes=boxes,
            masks=None
        )

        # mask decoder
        low_res_masks, iou_predictions = model.mask_decoder(
            image_embeddings=image_embedding,
            image_pe=model.prompt_encoder.get_dense_pe(),
            sparse_prompt_embeddings=sparse_embeddings,
            dense_prompt_embeddings=dense_embeddings,
            multimask_output=False
        )

        pred_masks = F.interpolate(
            low_res_masks,
            size=gt_instance_masks.shape[-2:],
            mode="bilinear",
            align_corners=False
        )

        pred_masks = pred_masks.squeeze(1)

        loss = segmentation_loss(pred_masks, gt_instance_masks)

        loss.backward()
        optimizer.step()

        total_loss += loss.item()

    return total_loss / len(dataloader)

