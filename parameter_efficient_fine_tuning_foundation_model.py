def get_instance_binary_masks(mask, instance_ids):
    masks = []

    for instance_id in instance_ids:
        binary = (mask == instance_id).float()
        masks.append(binary)

    if len(masks) == 0:
        return torch.zeros((0, mask.shape[0], mask.shape[1]))

    return torch.stack(masks, dim=0)

import torch.nn.functional as F

def dice_loss(pred, target, eps=1e-6):
    pred = torch.sigmoid(pred)

    pred = pred.view(pred.shape[0], -1)
    target = target.view(target.shape[0], -1)

    intersection = (pred * target).sum(dim=1)
    union = pred.sum(dim=1) + target.sum(dim=1)

    dice = (2 * intersection + eps) / (union + eps)

    return 1 - dice.mean()


def segmentation_loss(pred_logits, gt_masks):
    bce = F.binary_cross_entropy_with_logits(pred_logits, gt_masks)
    dloss = dice_loss(pred_logits, gt_masks)

    return bce + dloss

