from sklearn.model_selection import KFold
from torch.utils.data import Subset, DataLoader

def run_5fold_cross_validation(
    dataset,
    epochs=5,
    batch_size=1,
    lr=1e-4,
    max_boxes_per_image=20,
    r=4,
    alpha=8
):
    kfold = KFold(n_splits=5, shuffle=True, random_state=42)

    fold_results = []

    all_indices = np.arange(len(dataset))

    for fold, (train_idx, val_idx) in enumerate(kfold.split(all_indices)):
        print("=" * 60)
        print(f"FOLD {fold + 1}/5")
        print("=" * 60)

        train_subset = Subset(dataset, train_idx)
        val_subset = Subset(dataset, val_idx)

        train_loader = DataLoader(
            train_subset,
            batch_size=batch_size,
            shuffle=True,
            collate_fn=sam_collate_fn
        )

        val_loader = DataLoader(
            val_subset,
            batch_size=batch_size,
            shuffle=False,
            collate_fn=sam_collate_fn
        )

        model = build_lora_mobilesam(r=r, alpha=alpha)

        optimizer = torch.optim.AdamW(
            filter(lambda p: p.requires_grad, model.parameters()),
            lr=lr,
            weight_decay=1e-4
        )

        fold_history = []

        for epoch in range(epochs):
            train_loss = train_one_epoch(
                model,
                train_loader,
                optimizer,
                device,
                max_boxes_per_image=max_boxes_per_image
            )

            val_loss, val_dice = validate_one_epoch(
                model,
                val_loader,
                device,
                max_boxes_per_image=max_boxes_per_image
            )

            fold_history.append({
                "fold": fold + 1,
                "epoch": epoch + 1,
                "train_loss": train_loss,
                "val_loss": val_loss,
                "val_dice": val_dice
            })

            print(
                f"Fold {fold+1} | Epoch {epoch+1}/{epochs} | "
                f"Train Loss: {train_loss:.4f} | "
                f"Val Loss: {val_loss:.4f} | "
                f"Val Dice: {val_dice:.4f}"
            )

        metrics = evaluate_full_metrics(
            model,
            val_loader,
            device,
            max_boxes_per_image=max_boxes_per_image
        )

        trainable_params = sum(
            p.numel() for p in model.parameters() if p.requires_grad
        )

        metrics["Fold"] = fold + 1
        metrics["Trainable Params"] = trainable_params

        fold_results.append(metrics)

        fold_history_df = pd.DataFrame(fold_history)
        fold_history_df.to_csv(
            os.path.join(RESULTS_DIR, f"fold_{fold+1}_history.csv"),
            index=False
        )

        torch.save(
            model.state_dict(),
            os.path.join(MODELS_DIR, f"mobilesam_lora_fold_{fold+1}.pth")
        )

        if fold == 0:
            save_visual_examples(
                model,
                val_loader,
                device,
                save_dir=os.path.join(RESULTS_DIR, "visual_examples"),
                max_examples=5,
                max_boxes_per_image=max_boxes_per_image
            )

        del model
        torch.cuda.empty_cache()

    results_df = pd.DataFrame(fold_results)

    cols = [
        "Fold",
        "Dice",
        "AJI",
        "PQ",
        "Dice_std",
        "AJI_std",
        "PQ_std",
        "Trainable Params"
    ]

    results_df = results_df[cols]

    results_df.to_csv(
        os.path.join(RESULTS_DIR, "5fold_results.csv"),
        index=False
    )

    summary = {
        "Method": "MobileSAM + LoRA",
        "Dice Mean": results_df["Dice"].mean(),
        "Dice Std": results_df["Dice"].std(),
        "AJI Mean": results_df["AJI"].mean(),
        "AJI Std": results_df["AJI"].std(),
        "PQ Mean": results_df["PQ"].mean(),
        "PQ Std": results_df["PQ"].std(),
        "Trainable Params": results_df["Trainable Params"].iloc[0]
    }

    summary_df = pd.DataFrame([summary])

    summary_df.to_csv(
        os.path.join(RESULTS_DIR, "final_summary.csv"),
        index=False
    )

    return results_df, summary_df

